#pragma once
#include <vector>
#include <cassert>

// To be completed for higher orders
inline void gauss_legendre_table(int n, std::vector<double>& x, std::vector<double>& w) {
    assert(n >= 1 && n <= 6);
    x.resize(n); w.resize(n);
    if (n == 1) {
        x[0] = 0.0; w[0] = 2.0;
    } else if (n == 2) {
        x[0] = -0.5773502691896257; x[1] = -x[0];
        w[0] = 1.0; w[1] = 1.0;
    } else if (n == 3) {
        x[0] = -0.7745966692414834; x[1] = 0.0; x[2] = -x[0];
        w[0] = 0.5555555555555556; w[1] = 0.8888888888888888; w[2] = w[0];
    } else if (n == 4) {
        x[0] = -0.8611363115940526; x[1] = -0.3399810435848563;
        x[2] = -x[1]; x[3] = -x[0];
        w[0] = 0.3478548451374538; w[1] = 0.6521451548625461;
        w[2] = w[1]; w[3] = w[0];
    } else if (n == 5) {
        x[0] = -0.9061798459386640; x[1] = -0.5384693101056831; x[2] = 0.0;
        x[3] = -x[1]; x[4] = -x[0];
        w[0] = 0.2369268850561891; w[1] = 0.4786286704993665; w[2] = 0.5688888888888889;
        w[3] = w[1]; w[4] = w[0];
    } else if (n == 6) {
        x[0] = -0.9324695142031521; x[1] = -0.6612093864662645; x[2] = -0.2386191860831969;
        x[3] = -x[2]; x[4] = -x[1]; x[5] = -x[0];
        w[0] = 0.1713244923791704; w[1] = 0.3607615730481386; w[2] = 0.4679139345726910;
        w[3] = w[2]; w[4] = w[1]; w[5] = w[0];
    }
}
